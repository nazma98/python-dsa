# iterate through a string

for letter in "Nazma Sarker":
    print(letter)

# iterate through array
friends = ["promi", "mukta", "swarna", "sigma"]
for friend in friends:
    print(friend)

for index in range(len(friends)):
    print(friends[index])

# iterate though a range of number
for index in range(10):
    print(index)

# iterate though a specified range
print("New range")
for num in range(3, 10):
    print(num)

